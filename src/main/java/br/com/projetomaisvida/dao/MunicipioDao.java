	public void save(Municipio municipio) {
        Map<String, Object> mapMunicipio = new MunicipioConverter().converterToMap(municipio);
        save(mapMunicipio);
    }

    public List<Municipio> findMunicipios(Map<String, Object> mapKeyValue) {
    	List<DBObject> dbObject = findKeyValue(mapKeyValue);
        List<Municipio> municipios = new ArrayList<Municipio>();
        if(dbObject.isEmpty()){
			popularTabela();
			dbObject = findKeyValue(mapKeyValue);
		}

        for (DBObject dbo : dbObject) {
            Municipio municipio = new MunicipioConverter().converterToMunicipio(dbo);
            municipios.add(municipio);
        }

        return municipios;
    }
    
	public void popularTabela(){
    	JSONObject estados = new JSONObject(getMunicipios());
		String estado = "";
		
		for (Object key : estados.keySet()) {
			String keyStr = (String)key;
	        Object keyvalue = estados.get(keyStr);
	        JSONArray arrayUf= (JSONArray)keyvalue;
	        for (Object o : arrayUf) {
	        	JSONObject ufs = (JSONObject)o;
				for (Object uf : ufs.keySet()) {
					String keyUf = (String)uf;
					Object valueUf = ufs.get(keyUf);

					if(keyUf.trim().equals("sigla")){
						estado = valueUf.toString();

					}else if(keyUf.trim().equals("cidades")){
						JSONArray cidades = (JSONArray)valueUf;
						for (Object cidade : cidades) {
					        new MunicipioDao().save(new Municipio(cidade.toString(), estado));
						}
					}
				}
	        }
		}
    }
	
